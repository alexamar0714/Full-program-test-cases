package cn.edu.ruc.iir.rainbow.common.exception;

/**
 * Created by hank on 17-4-6.
 */
public class NotMultiThreadedException extends Exception
{

    public NotMultiThreadedException (String message)
    {
        super (message);
    }

    @Override
    public String getMessage ()
    {
        return super.getMessage();
    }

    @Override
    public String toString ()
    {
        return super.toString();
    }
}
